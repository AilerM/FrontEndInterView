// 左移
// 右移
var a = 10;
const v1 = a << 1; // a * (2 ^ b)
const v2 = a >> 1; // a / (2 ^ b)
console.log(v1);
console.log(v2);

// 按位与
8 & 7; // -> 0
// 1000 & 0111 -> 0000 -> 0

// 或
8 | 7; // -> 15
// 1000 | 0111 -> 1111 -> 15

// 按位异或
// 每一位都不同，结果才为 1

8 ^ 7; // -> 15
8 ^ 8; // -> 0
// 1000 ^ 0111 -> 1111 -> 15
// 1000 ^ 1000 -> 0000 -> 0

// 两个数不使用四则运算得出和

